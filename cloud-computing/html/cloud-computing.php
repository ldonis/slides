    <p class="fragment fade-up">Pagar por lo que se usa</p>
</section>

<section data-transition="slide-in fade-out">
    <h3>Platform as a Service (PaaS)</h3>
    <hr>
    <p class="fragment fade-up">Provee una plataforma que permite desarrollar, ejecutar y administrar aplicaciones</p>
    <p class="fragment fade-up">Ideal para pruebas unitarias</p>
    <p class="fragment fade-up">Integracion continua</p>
</section>


<section data-transition="slide-in fade-out">
    <h3>Software as a Service (SaaS)</h3>
    <hr>
    <p class="fragment fade-up">Es un modelo en el cual el software se entrega bajo una licencia de suscripcion</p>
    <p class="fragment fade-up">Software bajo demanda</p>
    <p class="fragment fade-up">
        <a class="fragment fade-up highlight-blue"
