<section data-transition="slide-in fade-out">
    <h3>Content as a Service</h3>
    <hr>
    <p class="fragment fade-up">Contenido almacenado en la nube que se entrega bajo de manda</p>
    <p class="fragment fade-up">a cualquier dispositivo o sistema</p>
</section>


<section data-transition="slide-in fade-out">
    <h3>Serverless</h3>
    <hr>
    <p class="fragment fade-up">El proveedor se encarga completamente de la gestion y ejecucion del programa</p>
    <p class="fragment fade-up">cualquier tipo de administracion de servidor esta oculta al usuario</p>
</section>


